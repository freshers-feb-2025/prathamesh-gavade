// Number Datatype
let id=24;
console.log(typeof id);

// String Datatype
let fullName="Prathamesh";
console.log(typeof fullName);

// Boolean Datatype
let istrue=true;
console.log(typeof istrue);

// Undefined
let x;
console.log(x);

// null
let n=null;
console.log(n);

// BigInt 
let n1= BigInt(123);
console.log(typeof n1);

// Symbol
let b= Symbol("Hello!");
console.log(typeof b);

// Non primitive datatypes

// Object
const student = {
    id : 1,
    fullName : "Prathamesh",
    salary : 8000
}
